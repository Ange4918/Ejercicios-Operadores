package ClasesOperadores;

public class Ejercicio2 {
	
	public static void main(String args[]) {
		
		int valor1 = 9;
		
		int valor2 = 7;
		
		int suma = valor1 + valor2;
		
		System.out.println("El valor de la suma de " + valor1 + " y " + valor2 + " es " + suma);
		
		int resta = valor1 - valor2;
		
		System.out.println("El valor de la resta de " + valor1 + " y " + valor2 + " es " + resta);
		
		int multiplicación = valor1 * valor2;
		
		System.out.println("El valor de la multiplicación de " + valor1 + " y " + valor2 + " es " + multiplicación);
		
		double división = valor1 / valor2;
		
		System.out.println("El valor de la división de " + valor1 + " y " + valor2 + " es " + división);
		
	}

}
